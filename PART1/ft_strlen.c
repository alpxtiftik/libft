#include "libft.h"

size_t	ft_strlen(const char *s)
{
	size_t	i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}

#include <stdio.h>

int main()
{
	char *s = "ALP";
	printf("%zu", ft_strlen(s));
}