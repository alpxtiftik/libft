#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	const unsigned char	*str;

	str = s;
	while (n > 0)
	{
		if (*str == (unsigned char)c)
			return ((void *)str);
		str++;
		n--;
	}
	return (NULL);
}
int main()
{
	char s[5] = "abcde";

	void *x = ft_memchr(s, 'e', 5);
	printf("%p", ((char *)x));

}